def main():
    channel = grpc.insecure_channel(ANCHOR_ENGINE_ADDR)
    stub = engines_pb2_grpc.AnchorEngineStub(channel)

    print(f"📌 注册 {len(ANCHORS)} 个新锚点...\n")
    success = 0
    for i, a in enumerate(ANCHORS, 1):
        req = engines_pb2.GenerateAnchorRequest(
            source_texts=[a["text"]],
            topic_hints=a["topics"],
            source_type="platform_initial",
        )
        try:
            resp = stub.GenerateAnchor(req)
            if resp.success:
                success += 1
                print(f"  [{i:2d}/{len(ANCHORS)}] ✅ {resp.anchor.anchor_id} (质量={resp.quality_score:.2f}) {a['text'][:25]}...")
            else:
                print(f"  [{i:2d}/{len(ANCHORS)}] ❌ {resp.rejection_reason}")
        except Exception as e:
            print(f"  [{i:2d}/{len(ANCHORS)}] ❌ {e}")
        time.sleep(0.3)

    print(f"\n🎉 完成: {success}/{len(ANCHORS)}")


if __name__ == "__main__":
    main()
